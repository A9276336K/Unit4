package sprint1;

public class _2Iterative {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("I love You");
        }
        int j=0;
        do {
            System.out.println("Yasmeen");
            j++;
        }
        while(j<5);
    }
}
